/**
 * 
 *  Muestra los números del 320 al 160 contando de 20 en 20 hacia atras,
 *  utilizando un bucle While.
 * 
 * @author Belén Gutierrez
 */
public class C5ejercicio05 {

  public static void main(String[] args) {
    
    int i = 320;
    
    
    while ( i >= 160 ) {
      System.out.println(i);
      i-=20;
    }
  }
}
